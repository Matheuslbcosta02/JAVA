public class Arraysteste{
    public static void main(String[] args){
        int[] array1 = {1,3,5,7,8};
        // acessando posição do vetor.
        System.out.println(array1[0]);

        // outras formas de declarar.
        int []array2 = {2,4,6,9};
        int array3[] = {10,12,30};

        //declarando vetor com tamanho e preenchendo ele.
        int[] array4 = new int[5];
        array4 [0]=30;
        array4 [1]=33;
        array4 [2]=20;
        array4 [3]=80;
        array4 [4]=70;
        System.out.println(array4[0]);
    }
}